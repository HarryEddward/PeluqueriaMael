    def getSourceModule(self):
        enable_test = """
        #define TEST_INVROUND
        """

        file = open("../kernels/general.cuh", "r")
        kernelwrapper = file.read()
        file.close()
        file = open("../kernels/InvSubbytes.cuh", "r")
        kernelwrapper += file.read()
        file.close()
        file = open("../kernels/InvShiftRows.cuh", "r")
        kernelwrapper += file.read()
        file.close()
        file = open("../kernels/MixColumns.cuh", "r") # inv Mix Columns depends on mix Columns!!
        kernelwrapper += file.read()
        file.close()
        file = open("../kernels/InvMixColumns.cuh", "r")
        kernelwrapper += file.read()
        file.close()
        file = open("../kernels/AddRoundKey.cuh", "r")
        kernelwrapper += file.read()
        file.close()
        file = open("../kernels/InvRound.cuh", "r")
        kernelwrapper += file.read()
        file.close()

        self.module = SourceModule(enable_test + kernelwrapper)


    def invRound_gpu(self, block, roundkey):
        # Event objects to mark the start and end points
        start = cuda.Event()
        end = cuda.Event()

        # Start recording execution time
        start.record()

        # Device memory allocation for input and output arrays
        io_block_gpu = cuda.mem_alloc_like(block)
        i_roundkey_gpu = cuda.mem_alloc_like(roundkey)
        i_rcon_gpu = self.module.get_global('rcon')[0]
        i_invSbox_gpu = self.module.get_global('invSbox')[0]
        i_mul2_gpu = self.module.get_global('mul2')[0]
        i_mul3_gpu = self.module.get_global('mul3')[0]

        # Copy data from host to device
        cuda.memcpy_htod(io_block_gpu, block)
        cuda.memcpy_htod(i_roundkey_gpu, roundkey)
        cuda.memcpy_htod(i_rcon_gpu, self.rcon)
        cuda.memcpy_htod(i_invSbox_gpu, self.invSbox)
        cuda.memcpy_htod(i_mul2_gpu, self.mul2)
        cuda.memcpy_htod(i_mul3_gpu, self.mul3)

        # Call the kernel function from the compiled module
        prg = self.module.get_function("InvRoundTest")

        # Calculate block size and grid size
        blockDim = (1, 1, 1)

        # Call the kernel loaded to the device
        prg(io_block_gpu, i_roundkey_gpu, block=blockDim)

        # Copy result from device to the host
        res = np.empty_like(block)
        cuda.memcpy_dtoh(res, io_block_gpu)

        # Record execution time (including memory transfers)
        end.record()
        end.synchronize()

        # return a tuple of output of sine computation and time taken to execute the operation (in ms).
        return res, start.time_till(end) * 10 ** (-3)


